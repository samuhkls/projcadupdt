    private void btnLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLoginMouseClicked
        String login = logEmail.getText();
        String senha = logSenha.getText();
                
        UsuarioDao u1 = new UsuarioDao();
        
        
        try {
            
             if ( login.equals("admin") && senha.equals("1234")) {
              
                              formCli objCli  = new formCli();
                              objCli.setVisible(true);
                              this.setVisible(false);
                     
                 }
              else
               {
                       ResultSet logincon = u1.validarLogin(login, senha);
                       
                       if  ( logincon.next() ){ 
                                                         
                            formCli objCli  = new formCli();
                            objCli.setVisible(true);
                            this.setVisible(false); 
                             }
                }
        
             }       
        catch ( SQLException err) {
                JOptionPane.showMessageDialog(null, "Dados Inv√°lidos Erro no login !\n"+ err.getMessage() );
                this.logEmail.grabFocus();;         }
     
    }//GEN-LAST:event_btnLoginMouseClicked

